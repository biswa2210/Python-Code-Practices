add   = lambda a,b : a+b#LAMBDA FUNCTION THIS FUNCTION RETURN a+b
minus = lambda a,b : a-b#LAMBDA FUNCTION THIS FUNCTION RETURN a-b
mul   = lambda a,b : a*b#LAMBDA FUNCTION THIS FUNCTION RETURN a*b
print("\n----------------------------------->\nGIVEN NUMBERS 10 AND 5 \n----------------------------------->")
print("MINUS          ::",minus(10,5))
print("ADDITION       ::",add(10,5))
print("MULTIPLICATION ::",mul(10,5))
print("----------------------------------->")